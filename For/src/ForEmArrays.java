public class ForEmArrays {
    public static void main(String[] args) {
        String alunos[] = {"Aline", "Felipe", "Rafael", "Soraya"};

        for (int i = 0; i < alunos.length; i++) { 
            System.out.println("O aluno do indice i é: " + i + " " + alunos[i]); //imprime o indíce e o nome do aluno 
        }
}
}
